$(function() {
  $('.mypage-modal').mouseover(function() {
    $('.header__row__mypage').css('display', 'block');
  });
});
$(function() {
  $('.mypage-modal').mouseout(function() {
    $('.header__row__mypage').css('display', 'none');
  });
});
